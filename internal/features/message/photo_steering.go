package message

import "math/rand"

// photoSteeringAntiTemplate — анти-шаблонное правило, которое попадает в КАЖДУЮ
// фото-подсказку. Оно гасит залипший каркас «О, «кличка» вылез! … это ты,
// блядь, …», который модель воспроизводит из-за самоподкрепления через историю.
const photoSteeringAntiTemplate = `Дополнительные правила именно для этой реакции на фото:
- Не используй один и тот же каркас ответа. НЕ начинай реплику со слова "О". Не давай изображению кличку в кавычках по лекалу "название" + "вылез"/"выискался". Меняй зачин, синтаксис и структуру от ответа к ответу.
- Держись правила длины: 1-3 коротких предложения, не более 300 символов. Оставайся в персонаже, тон не смягчай.`

// photoAngles — курируемый пул «углов атаки». На каждый вызов берётся максимум
// один; строки подобраны так, чтобы не быть подстроками друг друга (это важно
// для теста, который считает ровно одно вхождение).
var photoAngles = []string{
	"Прицепись к одной конкретной мелкой детали на фото и раздуй её до абсурда.",
	"Сравни изображённое с чем-то унизительным из быта.",
	"Притворись, что не разглядел или не понял, что на фото, и обыграй это.",
	"Цепляйся к подписи под фото, а не к самому изображению.",
	"Обыграй то, откуда переслана картинка, или зачем автор её вообще скинул.",
	"Изобрази фальшивый восторг, который тут же переходит в оскорбление.",
	"Вынеси изображению вердикт в роли надутого эксперта.",
	"Ответь одним издевательским риторическим вопросом по фото.",
	"Прицепись к цвету, композиции или качеству самого фото.",
	"Сведи всё к одному ёмкому приговору-ярлыку без зачина про кличку.",
	"Скажи, что фото напомнило тебе что-то жалкое про самого автора.",
	"Обыграй контраст между тем, что на фото, и убогой реальностью автора.",
}

// photoFreestyleChance — вероятность отдать только анти-шаблонное правило без
// конкретного угла, чтобы сами 12 углов не превратились в новый набор
// под-шаблонов.
const photoFreestyleChance = 0.15

// BuildPhotoSteering собирает call-scoped доп-инструкцию для одной реакции на
// фото: всегда анти-шаблонное правило и, с вероятностью 1-photoFreestyleChance,
// один случайный угол из пула.
func BuildPhotoSteering(r *rand.Rand) string {
	if r.Float64() < photoFreestyleChance {
		return photoSteeringAntiTemplate
	}
	angle := photoAngles[r.Intn(len(photoAngles))]
	return photoSteeringAntiTemplate + "\n- В этот раз построй реакцию так: " + angle
}
